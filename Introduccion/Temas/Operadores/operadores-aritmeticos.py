
a = 3
b = 2

# Operador de suma
suma = a + b
print( "Resultado de la suma:", suma)

# Operador de resta
resta = a - b
print( "Resultado de la resta:", resta)

# Operador de multiplicación
multiplicacion = a * b
print( "Resultado de la multiplicacion:", multiplicacion)

# Operador de división
division = a // b
print( "Resultado de la división:", division)

# Operador de modulo
modulo = a % b
print( "Resultado del modulo: ", modulo)

# Operador de exponente
exponente = a ** b
print( "Resultado del exponente: ", exponente)