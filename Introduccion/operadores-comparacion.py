# Variables a comparar
a = 3
b = 2

# Operador de igualdad
resultado = (a == b)
print("Son iguales:", resultado)

# Operador de diferente
resultado = (a != b)
print("Son diferentes:", resultado)

# Operador mayor que
resultado = (a > b)
print("A es mayor que B:", resultado)

# Operador mayor o igual que
resultado = (a >= b)
print("A es mayor o igual que B:", resultado)

# Operador menor que
resultado = (a < b)
print("A es menor que B:", resultado)

# Operador menor o igual que
resultado = (a <= b)
print("A es menor o igual que B:", resultado)